import SwiftUI

struct GlowOrb: View {
    let color: Color
    var size: CGFloat = 200

    // Ambient glow opacity — emissive on dark.
    private var glowOpacity: Double { 0.18 }

    var body: some View {
        Circle()
            .fill(
                RadialGradient(
                    colors: [color, .clear],
                    center: .center,
                    startRadius: 0,
                    endRadius: size / 2
                )
            )
            .frame(width: size, height: size)
            .blur(radius: 40)
            .opacity(glowOpacity)
            .allowsHitTesting(false)
    }
}
